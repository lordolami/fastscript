import { cpSync, existsSync, mkdirSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";

const PUBLIC_ROOT = resolve(".");
const PRIVATE_ROOT = resolve("..", "fastscript-core-private");
const FILES = [
  "src/build.mjs",
  "src/deploy.mjs",
  "src/server-runtime.mjs",
  "src/fs-parser.mjs",
  "src/typecheck.mjs",
  "src/auth.mjs",
  "src/storage.mjs",
  "src/webhook.mjs",
  "scripts/backup.mjs",
  "scripts/restore.mjs",
  "scripts/backup-verify.mjs",
  "scripts/zero-downtime-deploy.mjs",
  "scripts/secret-rotation.mjs",
  "docs/INCIDENT_PLAYBOOK.md",
  "docs/OBSERVABILITY.md",
  "docs/ROLLOUT_GUIDE.md",
  "docs/THREAT_MODEL.md",
  "docs/PROOF_PACK.md",
  "docs/SBOM.json"
];

mkdirSync(PRIVATE_ROOT, { recursive: true });
for (const entry of readdirSync(PRIVATE_ROOT, { withFileTypes: true })) {
  if (entry.name === ".git") continue;
  rmSync(join(PRIVATE_ROOT, entry.name), { recursive: true, force: true });
}

for (const rel of FILES) {
  const from = join(PUBLIC_ROOT, rel);
  const to = join(PRIVATE_ROOT, rel);
  if (!existsSync(from)) continue;
  mkdirSync(dirname(to), { recursive: true });
  cpSync(from, to, { recursive: true });
}

writeFileSync(join(PRIVATE_ROOT, "README.md"), `# FastScript Core Private\n\nThis workspace contains the sensitive FastScript compiler/runtime/platform materials that should not remain in the public-facing repository long term.\n\nOrigin: ${PUBLIC_ROOT}\nGenerated by scripts/sync-private-core.mjs.\n`, "utf8");

writeFileSync(join(PRIVATE_ROOT, "package.json"), JSON.stringify({
  name: "fastscript-core-private",
  version: "0.1.0-private",
  private: true,
  type: "module",
  description: "Protected FastScript compiler/runtime/platform core",
  license: "UNLICENSED",
  exports: {
    "./build": "./src/build.mjs",
    "./deploy": "./src/deploy.mjs",
    "./server-runtime": "./src/server-runtime.mjs",
    "./parser": "./src/fs-parser.mjs",
    "./typecheck": "./src/typecheck.mjs",
    "./auth": "./src/auth.mjs",
    "./storage": "./src/storage.mjs",
    "./webhook": "./src/webhook.mjs"
  }
}, null, 2), "utf8");

writeFileSync(join(PRIVATE_ROOT, ".gitignore"), ["node_modules/", "dist/", ".DS_Store"].join("\n"), "utf8");

writeFileSync(join(PRIVATE_ROOT, "BOUNDARY.json"), JSON.stringify({
  generatedAt: new Date().toISOString(),
  sourceRepo: PUBLIC_ROOT,
  files: FILES
}, null, 2), "utf8");

console.log(`private core workspace synced: ${PRIVATE_ROOT}`);
